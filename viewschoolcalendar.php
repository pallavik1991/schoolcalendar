            $(document).ready(function(){

                var acyear;

                 $.ajax({

                url:"get_last_academicyear.php",
                type:"GET",

                success:function(data){
                    var result=JSON.parse(data);
                    document.getElementById("txtacyear").value=result;
                    acyear=result;
                }
            });
                 
                 $.ajax({
                    type:"POST",
                  url: 'dal_viewschoolcal.php',
                   data:({
                         param_acyear:acyear
                   }),
                    success: function(data)          //on recieve of reply
                    {
                        result=JSON.parse(data);
                        var table_calendar=document.getElementById("table_calendar");
                         
                        for(var i=0;i<result.length; i++)
                        {
                            var slno=result[i].slno;
                            var eventdate=result[i].eventdate;
                            var eventname=result[i].eventname;
                            var reminder=result[i].reminder_required;
                            var days=result[i].daysbefore;
                            
                            
                            slno='<td>'+slno+'</td>';
                            eventdate='<td>'+eventdate+'</td>';
                            eventname='<td>'+eventname+'</td>';
                            reminder='<td>'+reminder+'</td>';
                            days='<td>'+days+'</td>';
                            
                            
                var row='<tr>'+slno+eventdate+eventname+reminder+days+'</tr>';
                            $('#table_calendar tr:last').after(row);
                        }

                    }   
                    
                 
            });
             });
