        $(document).ready(function(){

            $.ajax({

                url:"get_last_academicyear.php",
                type:"GET",

                success:function(data){
                    var result=JSON.parse(data);
                    document.getElementById("txtacyear").value=result;
                    
                }
            });


         $("#rdbreminder_yes").click(function(){
                if(this.checked)
                 document.getElementById("table_days").style.display="block";
                 
            });

            $("#rdbreminder_no").click(function(){
                if(this.checked)
                 document.getElementById("table_days").style.display="none";
                 
            });

             
            $("#btnadd").click(function(){
                var acyear="",eventdate="",eventname="",reminder="",days="";

                acyear=document.getElementById("txtacyear").value;
                eventdate=document.getElementById("txteventdate").value;
                eventname=document.getElementById("txteventname").value;

                if(document.getElementById("rdbreminder_yes").checked)
                    reminder=document.getElementById("rdbreminder_yes").value;
                else
                    reminder=document.getElementById("rdbreminder_no").value;
                days=document.getElementById("txtdays").value;

                
                  $.ajax({

                  type: "POST",
                  url: 'dal_addschoolcalendar.php',
                  data: ({
                    param_acyear:acyear,
                    param_eventdate:eventdate,
                    param_eventname:eventname,
                    param_reminder:reminder,
                    param_days:days
                    }),
                  success: function(data) {
                   document.getElementById("div1").innerHTML=data;
                   
                  } 
                }); 
                });   
        });
